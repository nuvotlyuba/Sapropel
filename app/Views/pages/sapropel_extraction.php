    <p class="sapropel-extraction__text content-text">Сапропель это экологически чистое удобрение, которое богато: натрием, калием, фосфором, витаминами (B, E, C, D, P), аминокислотами и ферментами. В нём содержатся гуминовые кислоты, обеззараживающие почву, стимулирующие рост растений, подавляющие развитие болезнетворных микроорганизмов. </p>
    <p class="sapropel-extraction__text content-text">Сапропель делает почвы более рыхлыми, формирует плодородный слой, увеличивает количество гумуса в грунте, позволяет сохранить плодородность земли на 3 – 5 лет. Субстрат, в который добавлен сапропель, удерживает влагу, поэтому почва реже требует полива.</p>
